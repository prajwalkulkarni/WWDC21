 ## What is Climate Change?
 Before looking at what climate change is, it is important to know what exactly climate is.
 Climate means the usual condition of the temperature, humidity, atmospheric pressure, wind, rainfall, and other meteorological elements in an area of the Earth's surface for a long time. In simple terms, climate is the average condition of a region for about thirty years.
**Climate change** describes an unusual change in the average conditions — such as temperature and rainfall — in a region over a long period of time.
 
 These include warming temperatures and changes in precipitation, as well as the effects of Earth’s warming, such as:

  1. Rising sea levels🌊
  2. Shrinking mountain glaciers🏔
  3. Ice melting at a faster rate than usual in Greenland, Antarctica and, the Arctic🥶
  4. Changes in flower and plant blooming times.🌷
 
 ---
 
 ## What's causing this unusual change in climate?
 Certain gases in Earth’s atmosphere block heat from escaping. This is called the greenhouse effect🔥. These gases keep Earth warm like the glass in a greenhouse keeps plants warm.

 Human activities — such as burning fuel to power factories🏭, cars🚗 and, buses🚌 — are changing the natural greenhouse. These changes cause the atmosphere to trap more heat than it used to, leading to a warmer Earth.
 
 
 ---
 
 
 ## So, how to minimize climate change?
 As mentioned above, it's the human activities that are abruptly causing the unusual change in Earth's greenhouse. This could be minimized by making better lifestyle choices(choosing options that have minimal or zero impact on greenhouse), or/and, compensating for our carbon footprint (planting trees).🌱🌱
 
